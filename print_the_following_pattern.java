import java.util.*;
class main
{
    public static void main(String args[])
    {
        Scanner sc=new Scanner(System.in);
        int n,i,j;
        n=sc.nextInt();
        for(i=0; i<n; i++)
        {
            for(j=1; j<=n; j++)
            {
                System.out.print(n-j+1+" ");
            }
            System.out.format("
");
        }
    }
}